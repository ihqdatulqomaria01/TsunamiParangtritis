                              <h4>Hal Yang Dilakukan Sebelum Terjadi Tsunami</h4>
                        </div>
                        <div class="card-body">
                        <p class="justify-text">1. Mengenali tanda-tanda sebelum terjadi tsunami, yakni surutnya air laut yang melewati garis pantai normal dan tercium aroma garam yang kuat.</p>
                        <p class="justify-text">2. Mengetahui jalur evakuasi ke tempat yang aman jika terjadi tsunami serta mengetahui gedung tinggi (minimal 3 lantai) dengan konstruksi yang kuat.</p>
                        <p class="justify-text">3. Menyiapkan perlengkapan darurat seperti peta rute evakuasi dan nomor kontak penting.</p>
                        <p class="justify-text">4. Tetap update dengan informasi cuaca dan peringatan tsunami melalui radio, televisi, atau aplikasi di ponsel.</p>
                        </div>
                    </div>
