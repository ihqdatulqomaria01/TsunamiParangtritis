                <div class="col-12">
                    <div class="card">
                        <div class="card-header">
                              <h4>Hal Yang Dilakukan Saat Tsunami Datang</h4>
                        </div>
                        <div class="card-body">
                        <p class="justify-text">1. Tetap tenang dan tidak panik, agar dapat bertindak cepat saat tsunami datang.</p>
                        <p class="justify-text">2. Bergerak sesuai dengam jalur evakuasi tsumami. Jika tidak mengetahui jalur evakuasi, bergerak ke tempat yang lebih tinggi.</p>
                        <p class="justify-text">3. Apabila tidak menemukan tempat yang lebih tinggi, maka mencari gedung tinggi (minimal tiga lantai) dengan konstruksi yang kuat. Berlindung di tempat aman dan menunggu hingga keadaan membaik.</p>
                        <p class="justify-text">4. Apabila hanyut di dalam gelombang, maka cari benca-benca terapung yang dapat dijadikan rakit, seperti batang pohon.</p>
                        <p class="justify-text">5. Jangan minum air laur dan tetap di permukaan air untuk bernafas.</p>
                        <p class="justify text">6. Apabila gelombang membawa ke tempat tinggi seperti atap rumah atau gedung, berusaha bertahan di tempat itu hingga air surut dan keadaan tenang.</p>
                        </div>
                    </div>
                </div>
                <div class="col-12">
                    <div class="card">
                        <div class="card-header">
                            <h4>Hal Yang Dilakukan Setelah Tsunami</h4>
                        </div>
                        <div class="card-body">
                            <p class="justify-text">1. Jangan larut dalam kepanikan dan kesedihan, usahakan untuk tetap tenang dan kuat dalam menghadapi kenyataan.</p>
                            <p class="justify-text">2. Tetap berada di tempat evakuasi/tempat aman, hingga dinyatakan aman untuk kembali.</p>
                            <p class="justify-text">3. Apabila ingin kembali ke rumah, sebaiknya mengikuti imbauan regu penyelamat dan jangan melewati jalan-jalan yang rusak.</p>
                            <p class="justify-text">4. Apabila telah sampai rumah, waspadai jika ada bagian bangunan yang roboh dan lantai yang licin, serta hindari instalasi dan kabel listrik untuk menghindari sengatan listrik.</p>
                            <p class="justify-text">5. Memberi dukungan terhadap orang lain, terutama yang mengalami kehilangan, tekanan, dan penderitaan.</p>
