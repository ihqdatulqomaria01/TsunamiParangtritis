                        </div>
                        <div class="card-body">
                        <p class="justify-text">Pantai di Desa Parangtritis memiliki karakteristik geografis yang memperbesar risiko terjadinya tsunami. Lokasinya yang berdekatan dengan Sungai Opak menjadi faktor yang meningkatkan kerentanan, karena sungai dapat memperkuat arus tsunami yang merambat menuju daratan (Hisbulloh Akbar et al., 2020). Adanya morfologi pantai yang landai dan keberadaan material lepas di sepanjang pantai juga meningkatkan risiko tsunami, karena gelombang tsunami cenderung merambat masuk ke daratan dengan lebih jauh pada daerah dengan morfologi seperti itu (Sihwanti et al., 2022).</p>
                        <p class="justify-text">Pada tahun 2023, Pantai Parangtritis mendapatkan pengunjung sebanyak 2.219.880 jiwa dari jumlah total 2.701.842 pengunjung dari berbagai tempat wisata yang ada di Kabupaten Bantul (BPS, 2024). Berdasarkan data tersebut, dapat disimpulkan tempat wisata Pantai Parangtritis memiliki pengunjung sebesar 82% dari jumlah total pengunjung yang ada di Kabupaten Bantul. Dengan demikian, mitigasi bencana di Desa Parangtritis menjadi penting untuk dilakukan. Sebagai upaya mitigasi dan persiapan, analisis risiko bencana tsunami di sekitar Pantai Parangtritis sangatlah penting untuk dilakukan.</p>
